DROP TABLE IF EXISTS Users;
CREATE TABLE Users (User_id INTEGER PRIMARY KEY, 
                    User_name TEXT, 
                    Password TEXT,
                    Email TEXT NULL,
                    First_name TEXT Null,
                    Last_name TEXT Null,
                    Date_of_birth TEXT Null,
                    Phone_number TEXT Null,
                    Address TEXT Null,
                    UNIQUE(User_name));
DROP TABLE IF EXISTS Employees;                    
CREATE TABLE Employees (User_id INTEGER PRIMARY KEY,
                        Employee_id INTEGER,
                        UNIQUE (Employee_id),
                        FOREIGN KEY (User_id) REFERENCES Users ON DELETE CASCADE);                    
DROP TABLE IF EXISTS Admins;                    
CREATE TABLE Admins (User_id INTEGER PRIMARY KEY,
                        Admin_id INTEGER,
                        UNIQUE (Admin_id),
                        FOREIGN KEY (User_id) REFERENCES Employees ON DELETE CASCADE);
DROP TABLE IF EXISTS Doctors;                    
CREATE TABLE Doctors (User_id INTEGER PRIMARY KEY,
                        Doctor_id INTEGER,
                        certificate TEXT DEFAULT NULL,                  -- Path to certificate file
                        verify_state BOOLEAN DEFAULT 1,                 -- Verification status (1: verified, 0: not verified)
                        UNIQUE (Doctor_id),
                        FOREIGN KEY (User_id) REFERENCES Employees ON DELETE CASCADE);
DROP TABLE IF EXISTS Patients;
CREATE TABLE Patients (User_id INTEGER PRIMARY KEY,
                        Patient_id INTEGER,
                        UNIQUE (Patient_id),
                        FOREIGN KEY (User_id) REFERENCES Users ON DELETE CASCADE);
DROP TABLE IF EXISTS Duration;
CREATE TABLE Duration (Duration_id INTEGER PRIMARY KEY,
                       Start_date TEXT,
                       End_date TEXT);
DROP TABLE IF EXISTS Diagnosis;
CREATE TABLE Diagnosis (Diagnose_id INTEGER PRIMARY KEY,
  						Diagnose_date TEXT,
                        Diagnose TEXT);
DROP TABLE IF EXISTS Appointment_details;
CREATE TABLE Appointment_details (Examination_id INTEGER PRIMARY KEY,
                                  Date_last_modified TEXT,
                                  Date_of_meeting TEXT,
								  Location TEXT,
								  Confirmed INT,
                                  Treatment_notes TEXT,
                                  Time_of_meeting TEXT);
DROP TABLE IF EXISTS Treats;
CREATE TABLE Treats (Patient_id INTEGER,
                    Doctor_id INTEGER,
					Duration_id INTEGER,
                    PRIMARY KEY (Patient_id, Doctor_id, Duration_id),
                    FOREIGN KEY (Duration_id) REFERENCES Duration,
                    FOREIGN KEY (Doctor_id) REFERENCES Doctors,
                    FOREIGN KEY (Patient_id) REFERENCES Patients);
DROP TABLE IF EXISTS Diagnoses;
CREATE TABLE Diagnoses (Patient_id INTEGER,
                    Doctor_id INTEGER,
                    Diagnose_id INTEGER,
                    PRIMARY KEY (Patient_id, Doctor_id, Diagnose_id),
                    FOREIGN KEY (Diagnose_id) REFERENCES Diagnosis,
                    FOREIGN KEY (Doctor_id) REFERENCES Doctors,
                    FOREIGN KEY (Patient_id) REFERENCES Patients);
DROP TABLE IF EXISTS Meets;
CREATE TABLE Meets (Patient_id INTEGER,
                    Doctor_id INTEGER,
                    Examination_id INTEGER,
                    PRIMARY KEY (Patient_id, Doctor_id, Examination_id),
                    FOREIGN KEY (Examination_id) REFERENCES Appointment_details,
                    FOREIGN KEY (Doctor_id) REFERENCES Doctors,
                    FOREIGN KEY (Patient_id) REFERENCES Patients);
DROP TABLE IF EXISTS Questionaire_responses;
CREATE TABLE Questionaire_responses (Questionaire_id INTEGER,
                                     Questionaire_date TEXT,
                                     Attempt_id INTEGER PRIMARY KEY);
DROP TABLE IF EXISTS Question_answers;
CREATE TABLE Question_answers (Attempt_id INTEGER,
                               Answer TEXT,
                               Question_id INTEGER,
                               Answer_id INTEGER PRIMARY KEY,
                               FOREIGN KEY (Attempt_id) REFERENCES Questionaire_responses ON DELETE CASCADE);
DROP TABLE IF EXISTS Questionaire;
CREATE TABLE Questionaire (Questionaire_id INTEGER PRIMARY KEY,
                           Questionaire_name TEXT);
DROP TABLE IF EXISTS Questions;
CREATE TABLE Questions (Question_number INTEGER PRIMARY KEY,
                        Question TEXT,
                        Questionaire_id INTEGER,
                        FOREIGN KEY (Questionaire_id) REFERENCES Questionaire ON DELETE CASCADE);
DROP TABLE IF EXISTS Have;
CREATE TABLE Have (Question_number INTEGER,
                   Option_id INTEGER,
                   PRIMARY KEY (Question_number, Option_id),
                   FOREIGN KEY (Question_number) REFERENCES Questions ON DELETE CASCADE,
                   FOREIGN Key (Option_id) REFERENCES Question_options ON DELETE CASCADE);
DROP TABLE IF EXISTS Question_options;
CREATE TABLE Question_options (Option_id INTEGER PRIMARY KEY,
							   Answer TEXT,
                               Question_number INTEGER,
                               FOREIGN KEY (Question_number) REFERENCES Questions ON DELETE CASCADE);
DROP TABLE IF EXISTS has_access_to;
CREATE TABLE has_access_to (Patient_id INTEGER,
                            Questionaire_id INTEGER,
                            PRIMARY KEY (Patient_id, Questionaire_id),
                            FOREIGN KEY (Patient_id) REFERENCES Patients,
                            Foreign Key (Questionaire_id) REFERENCES Questionaire);
DROP TABLE IF EXISTS Fills_in;
CREATE TABLE Fills_in (Patient_id INTEGER,
                       Questionaire_id INTEGER,
                       Attempt_id INTEGER,
                       PRIMARY KEY (Patient_id, Questionaire_id, Attempt_id),
                       FOREIGN KEY (Patient_id) REFERENCES Patients,
                       Foreign Key (Questionaire_id) REFERENCES Questionaire
                       Foreign Key (Attempt_id) REFERENCES Questionaire_responses);
